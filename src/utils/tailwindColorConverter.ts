const hexToTailwind = (hex: string, isText: boolean): string => {
    const tailwindColors = isText ? tailwindTextColors : tailwindBgColors;
    const nearestColor = Object.keys(tailwindColors).reduce((prev, curr) =>
        Math.abs(parseInt(curr.replace('#', ''), 16) - parseInt(hex.replace('#', ''), 16)) <
            Math.abs(parseInt(prev.replace('#', ''), 16) - parseInt(hex.replace('#', ''), 16))
            ? curr
            : prev
    );
    return tailwindColors[nearestColor];
};

export const tailwindToHex = (tailwindClass: string): string => {
    const tailwindColors = { ...tailwindBgColors, ...tailwindTextColors };
    return Object.keys(tailwindColors).find(key => tailwindColors[key] === tailwindClass) || '#ffffff';
};

export default hexToTailwind;
